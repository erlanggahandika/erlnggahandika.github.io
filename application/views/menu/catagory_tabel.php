                type: 'ajax',
                method: 'get',
                async: false,
                url: '<?php echo base_url() ?>menu/deletya/',
                data: {
                    id: id
                },
                dataType: 'json',
                success: function(response) {
                    $('#myModalDelete').modal('hide');
                    location.reload();
                }
            });
        });
